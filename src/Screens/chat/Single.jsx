import React, {useState, useEffect, useRef} from 'react';
import {
  View,
  Text,
  TextInput,
  TouchableOpacity,
  FlatList,
  StyleSheet,
  Alert,
  Image,
  TouchableWithoutFeedback,
  Keyboard,
} from 'react-native';
import {useRoute, useNavigation} from '@react-navigation/native';
import {getFirestore} from '@react-native-firebase/firestore';
import {
  encryptMessage,
  decryptMessage,
  generateSecretKey,
} from '../../cryption/Encryption';
import Icon from 'react-native-vector-icons/MaterialCommunityIcons';
import {oStackHome} from '../../navigations/HomeNavigation';
import database from '@react-native-firebase/database';
globalThis.RNFB_SILENCE_MODULAR_DEPRECATION_WARNINGS = true;
const Single = () => {
  const route = useRoute();
  const {userId, myId, username, img} = route.params;
  const [messages, setMessages] = useState([]);
  const [text, setText] = useState('');
  const navigation = useNavigation();
  const chatId = userId < myId ? `${userId}_${myId}` : `${myId}_${userId}`;
  const secretKey = generateSecretKey(userId, myId); // T·∫°o secretKey cho ph√≤ng chat
  const [isSelfDestruct, setIsSelfDestruct] = useState(false);
  const [shouldAutoScroll, setShouldAutoScroll] = useState(true);
  const [isTyping, setIsTyping] = useState(false);
  const [remainingMessages, setRemainingMessages] = useState(0)
  const [countdown, setCountdown] = useState(0);

  const listRef = useRef(null);
  const timerRef = useRef(null);  

  // üîπ L·∫•y tin nh·∫Øn realtime
  useEffect(() => {
    const typingRef = database().ref(`/chats/${chatId}/typing`);

    const userRef = database().ref(`/users/${myId}`);
  
    const onUserDataChange = userRef.on('value', snapshot => {
      if (snapshot.exists()) {
        const userData = snapshot.val();
        const maxCount = userData.count || 5;
        const countChat = userData.countChat || 0;
        const remaining = maxCount - countChat;
        setRemainingMessages(remaining);
        if( remaining === 0){
          setCountdown(10);
           // N·∫øu c√≥ b·ªô ƒë·∫øm tr∆∞·ªõc ƒë√≥, clear tr∆∞·ªõc khi t·∫°o m·ªõi
        if (timerRef.current) clearInterval(timerRef.current);
        let timeLeft = 10;
        timerRef.current = setInterval(() => {
          timeLeft -= 1;
          setCountdown(timeLeft);

          if (timeLeft <= 0) {
            clearInterval(timerRef.current);
            setRemainingMessages(maxCount); // Reset l·∫°i s·ªë tin nh·∫Øn c√≥ th·ªÉ g·ª≠i
            userRef.update({ countChat: 0 }); // Reset tr√™n Firebase
          }
        }, 1000);
        }
      }
    });

    typingRef.on('value', snapshot => {
      if (snapshot.exists()) {
        const typingData = snapshot.val();
        setIsTyping(typingData.isTyping && typingData.userId !== myId);
      } else {
        setIsTyping(false);
      }
    });

    if (shouldAutoScroll && listRef.current) {
      setTimeout(() => {
        listRef.current.scrollToEnd({animated: true});
        setShouldAutoScroll(false); // T·∫Øt auto-scroll sau khi load
      }, 500);
    }
    const messagesRef = database().ref(`/chats/${chatId}/messages`);

    const onMessageChange = messagesRef.on('value', snapshot => {
      if (snapshot.exists()) {
        const msgs = Object.entries(snapshot.val()).map(([id, data]) => ({
          id,
          senderId: data.senderId,
          text: decryptMessage(data.text, secretKey),
          timestamp: new Date(data.timestamp),
          selfDestruct: data.selfDestruct || false, // Ki·ªÉm tra tin nh·∫Øn t·ª± h·ªßy
        }));

        setMessages(msgs.sort((a, b) => a.timestamp - b.timestamp));

        // X√≥a c√°c tin nh·∫Øn t·ª± h·ªßy sau 5 gi√¢y n·∫øu c√≥
        msgs.forEach(msg => {
          if (msg.selfDestruct) {
            setTimeout(() => {
              database().ref(`/chats/${chatId}/messages/${msg.id}`).remove();
            }, 5000);
          }
        });
      }
    });
    

    return () => {
      messagesRef.off('value', onMessageChange);
      typingRef.off(); // Cleanup khi r·ªùi kh·ªèi m√†n h√¨nh
      userRef.off('value', onUserDataChange);
    };
  }, [chatId, secretKey, shouldAutoScroll,myId]);


  useEffect(() => {
    let timer;
    if (remainingMessages === 0) {
      setCountdown(10); // B·∫Øt ƒë·∫ßu ƒë·∫øm ng∆∞·ª£c t·ª´ 10 gi√¢y
      timer = setInterval(() => {
        setCountdown(prev => {
          if (prev === 1) {
            clearInterval(timer);
            setRemainingMessages(5); // Reset l∆∞·ª£t nh·∫Øn tin sau khi ƒë·∫øm ng∆∞·ª£c xong
            return 0;
          }
          return prev - 1;
        });
      }, 1000);
    }
    return () => clearInterval(timer);
  }, [remainingMessages]);



  // üîπ G·ª≠i tin nh·∫Øn
  const sendMessage = async () => {
    if (!text.trim() || remainingMessages === 0) return;

    setShouldAutoScroll(true); // K√≠ch ho·∫°t auto-scroll

    try {
      const userRef = database().ref(`/users/${myId}`);
      const chatRef = database().ref(`/chats/${chatId}`);
      const chatSnapshot = await chatRef.once('value');
      const userSnapshot = await userRef.once('value');

      let userData = userSnapshot.val();
      const countChat = userData.countChat || 0;

      if (!userSnapshot.exists()) {
        Alert.alert('L·ªói', 'Kh√¥ng t√¨m th·∫•y th√¥ng tin ng∆∞·ªùi d√πng.');
        return;
      }

      if (!chatSnapshot.exists()) {
        // N·∫øu cu·ªôc tr√≤ chuy·ªán ch∆∞a t·ªìn t·∫°i, t·∫°o m·ªõi
        await chatRef.set({
          users: {[userId]: true, [myId]: true},
      
        });
      }

      // G·ª≠i tin nh·∫Øn
      const newMessageRef = chatRef.child('messages').push();
      await newMessageRef.set({
        senderId: myId,
        text: encryptMessage(text, secretKey),
        timestamp: database.ServerValue.TIMESTAMP,
        selfDestruct: isSelfDestruct,
      });

      // C·∫≠p nh·∫≠t s·ªë l∆∞·ª£ng tin nh·∫Øn ƒë√£ g·ª≠i
      await userRef.update({countChat: countChat + 1});

      setText('');
    } catch (error) {
      console.error('L·ªói khi g·ª≠i tin nh·∫Øn:', error);
    }
  };

  // üîπ X√≥a tin nh·∫Øn c·∫£ hai
  const deleteMessageForBoth = async messageId => {
    try {
      await database().ref(`/chats/${chatId}/messages/${messageId}`).remove();
      setMessages(prevMessages =>
        prevMessages.filter(msg => msg.id !== messageId),
      );
    } catch (error) {
      console.error('L·ªói khi x√≥a tin nh·∫Øn:', error);
    }
  };

  // üîπ X√°c nh·∫≠n x√≥a tin nh·∫Øn
  const confirmDeleteMessage = messageId => {
    Alert.alert('X√≥a tin nh·∫Øn', 'B·∫°n mu·ªën x√≥a tin nh·∫Øn n√†y?', [
      {text: 'H·ªßy', style: 'cancel'},
      {text: 'X√≥a', onPress: () => deleteMessageForBoth(messageId)},
    ]);
  };

  const handleTyping = isTyping => {
    database().ref(`/chats/${chatId}`).update({
      typing: {
        userId: myId,
        isTyping: isTyping,
      },
      users: {[userId]: true, [myId]: true},
    });
  };
  

  return (
    <TouchableWithoutFeedback onPress={Keyboard.dismiss}>
      <View style={styles.container}>
        <View style={styles.header}>
          <TouchableOpacity
            onPress={() => navigation.navigate(oStackHome.TabHome.name)}
            style={styles.backButton}>
            <Icon name="arrow-left" size={28} color="#000" />
          </TouchableOpacity>

          <View style={styles.userInfo}>
            <Image source={{uri: img}} style={styles.headerAvatar} />
            <Text style={styles.headerUsername}>{username}</Text>
          
          </View>

          <Text style={styles.remainingText}>
        {remainingMessages > 0
          ? `C√≤n ${remainingMessages} l∆∞·ª£t nh·∫Øn tin`
          : `H√£y ch·ªù ${countdown}s ƒë·ªÉ ti·∫øp t·ª•c`}
      </Text>
        </View>

        <FlatList
          ref={listRef}
          data={messages}
          keyExtractor={item => item.id}
          renderItem={({item}) => (
            <View
              style={
                item.senderId === myId
                  ? styles.sentWrapper
                  : styles.receivedWrapper
              }>
              {item.senderId !== myId && (
                <Image source={{uri: img}} style={styles.avatar} />
              )}
              <TouchableOpacity
                onLongPress={() => confirmDeleteMessage(item.id)}
                style={
                  item.senderId === myId
                    ? styles.sentContainer
                    : styles.receivedContainer
                }>
                {item.senderId !== myId && (
                  <Text style={styles.usernameText}>{username}</Text>
                )}
                <Text
                  style={
                    item.senderId === myId
                      ? styles.SendmessageText
                      : styles.ReceivedmessageText
                  }>
                  {item.text}
                </Text>
                <Text
                  style={
                    item.senderId === myId
                      ? styles.SendmessageText
                      : styles.ReceivedmessageText
                  }>
                  <Text
                    style={
                      item.senderId === myId
                        ? styles.Sendtimestamp
                        : styles.Revecivedtimestamp
                    }>
                    {item.timestamp.toLocaleTimeString([], {
                      hour: '2-digit',
                      minute: '2-digit',
                    })}
                  </Text>
                </Text>
              </TouchableOpacity>
            </View>
          )}
          
          showsHorizontalScrollIndicator={false}
          showsVerticalScrollIndicator={false}
          
        />
 {isTyping && <Text style={styles.typingText}>ƒêang nh·∫≠p...</Text>}
        <View style={styles.inputContainer}>
          
          <TouchableOpacity
            onPress={() => setIsSelfDestruct(!isSelfDestruct)}
            style={styles.iconButton}>
            <Icon
              name={isSelfDestruct ? 'timer-sand' : 'timer-off'}
              size={24}
              color={isSelfDestruct ? 'red' : '#007bff'}
            />
          </TouchableOpacity>

          <View style={styles.inputWrapper}>
            <TextInput
              style={styles.input}
              value={text}
              onChangeText={value => {
                setText(value); // C·∫≠p nh·∫≠t tin nh·∫Øn
                handleTyping(value.length > 0); // C·∫≠p nh·∫≠t tr·∫°ng th√°i nh·∫≠p
              }}
              placeholder="Nh·∫≠p tin nh·∫Øn..."
              onBlur={() => handleTyping(false)} // Khi m·∫•t focus th√¨ d·ª´ng nh·∫≠p 
              editable={remainingMessages > 0} // Ch·ªâ cho nh·∫≠p khi c√≤n l∆∞·ª£t nh·∫Øn
            />
          </View>

          <TouchableOpacity
          
            onPress={sendMessage}
            disabled={!text.trim() || remainingMessages === 0}
            style={[styles.disendButton, remainingMessages === 0 && styles.disabledButton]}>
            <Icon
              name="send"
              size={24}
              color={text.trim() || remainingMessages  === 0 ? '#007bff' : '#aaa'}
            />
          </TouchableOpacity>
        </View>
      </View>
    </TouchableWithoutFeedback>
  );
};

